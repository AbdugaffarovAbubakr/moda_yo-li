export function formatNewUserMessage(user) {
  return [
    'Yangi foydalanuvchi botga kirdi',
    '',
    `Telegram ID: ${user.telegramId}`,
    `Username: ${user.username ? `@${user.username}` : '-'}`,
    `Ism: ${user.firstName || '-'}`,
    `Familiya: ${user.lastName || '-'}`,
  ].join('\n');
}

export function formatApplicationMessage(user, application, applicationId) {
  return [
    'Yangi ariza topshirildi',
    '',
    `🆔 Ariza ID: ${applicationId}`,
    '',
    `Telegram ID: ${user.telegramId}`,
    `Username: ${user.username ? `@${user.username}` : '-'}`,
    `Ism: ${user.firstName || '-'}`,
    `Familiya: ${user.lastName || '-'}`,
    `Til: ${user.language || '-'}`,
    `Hudud: ${application.region}`,
    `Tuman/shahar: ${application.district}`,
    `FIO: ${application.fullName}`,
    `Tug'ilgan sana: ${application.birthDate}`,
    `Jinsi: ${application.gender === 'male' ? 'Erkak' : 'Ayol'}`,
    `Talaba: ${application.isStudent ? 'Ha' : "Yo'q"}`,
    `O'quv muassasasi: ${application.educationInstitution || '-'}`,
    `Mutaxassisligi: ${application.specialty || '-'}`,
    `Kursi: ${application.course || '-'}`,
    `Telefon: ${application.phone}`,
    `Telegram username: ${application.telegramUsername || '-'}`,
    `Elektron pochta: ${application.email || '-'}`,
    `Moda/ijodga qiziqadi: ${application.interestedInFashion ? 'Ha' : "Yo'q"}`,
    `Yo'nalish: ${application.fashionDirection || '-'}`,
    `Tajribasi bor: ${application.hasExperience ? 'Ha' : "Yo'q"}`,
    `Pasport/ID: ${application.passportFile ? 'Yuborildi' : '-'}`,
    `3x4 rasm: ${application.portraitFile ? 'Yuborildi' : '-'}`,
    `Tayyor ishlar: ${application.hasReadyWorks ? 'Ha' : "Yo'q"}`,
    `Fayllar soni: ${application.workFiles?.length || 0}`,
    `O'zi haqida: ${application.aboutSelf}`,
    `Motivatsiya: ${application.motivation}`,
  ].join('\n');
}

